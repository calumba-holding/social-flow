function getBanner(style = 'slant') {
  if (style === 'clean') {
    return [
      'SOCIAL FLOW',
      '-----------',
      'Meta APIs from your terminal'
    ].join('\n');
  }

  if (style === 'compact') {
    return [
      '+-------------------------------+',
      '|          SOCIAL FLOW          |',
      '+-------------------------------+'
    ].join('\n');
  }

  if (style === 'classic') {
    return [
      ' ____   ___   ____ ___    _    _       _____ _     _____        __ ',
      '/ ___| / _ \\ / ___|_ _|  / \\  | |     |  ___| |   / _ \\ \\      / /',
      '\\___ \\| | | | |    | |  / _ \\ | |     | |_  | |  | | | \\ \\ /\\ / / ',
      ' ___) | |_| | |___ | | / ___ \\| |___  |  _| | |__| |_| |\\ V  V /  ',
      '|____/ \\___/ \\____|___/_/   \\_\\_____| |_|   |_____\\___/  \\_/\\_/   '
    ].join('\n');
  }

  // Pre-generated banner; no runtime dependency.
  return [
    '  _____            _       _    _____ _      _____        __ ',
    ' / ____|          (_)     | |  |  ___| |    / _ \\ \\      / /',
    '| (___   ___   ___ _  __ _| |  | |_  | |   | | | \\ \\ /\\ / / ',
    ' \\___ \\ / _ \\ / __| |/ _` | |  |  _| | |   | | | |\\ V  V /  ',
    ' ____) | (_) | (__| | (_| | |  | |   | |___| |_| | | | |   ',
    '|_____/ \\___/ \\___|_|\\__,_|_|  |_|   |_____\\___/  |_| |_|   '
  ].join('\n');
}

module.exports = {
  getBanner
};
